    protected function initDefaultButtons()
    {
        $this->createDefaultButton('view', 'eye-open', ['class'=>'btn btn-primary text-white btn-sm btn-icon view']);
        $this->createDefaultButton('update', 'pencil', ['class'=>'btn btn-primary text-white btn-sm btn-icon update']);
        $this->createDefaultButton('remove', 'trash', [
            'data-confirm' => Yii::t('yii', 'Are you sure you want to delete this item?'),
            'data-method' => 'post',
            'class'=>'btn btn-primary text-white btn-sm btn-icon delete'
        ]);
    }

    protected function createDefaultButton($name, $iconName, $additionalOptions = [])
    {
        if (!isset($this->buttons[$name]) && strpos($this->template, '{' . $name . '}') !== false) {
            $this->buttons[$name] = function ($url, $model, $key) use ($name, $iconName, $additionalOptions) {
                switch ($name) {
                    case 'view':
                        $title = Yii::t('yii', 'View');
                        break;
                    case 'update':
                        $title = Yii::t('yii', 'Update');
                        break;
                    case 'remove':
                        $title = Yii::t('yii', 'Delete');
                        break;
                    default:
                        $title = ucfirst($name);
                }
                $options = array_merge([
                    'title' => $title,
                    'aria-label' => $title,
                    'data-pjax' => '0',
                ], $additionalOptions, $this->buttonOptions);
                $icon = isset($this->icons[$iconName])
                    ? $this->icons[$iconName]
                    : Html::tag('span', '', ['class' => "glyphicon glyphicon-$iconName"]);
                return Html::a($icon, $url, $options);
            };
        }
    }
    
    public function renderDataCell($model, $key, $index)
    {
        if ($this->contentOptions instanceof Closure) {
            $options = call_user_func($this->contentOptions, $model, $key, $index, $this);
        } else {
            $options = $this->contentOptions;
        }
        $content = Html::tag('div', $this->renderDataCellContent($model, $key, $index), ['class'=>'toolbox']);
        return Html::tag('th', $content, $options);
    }
}